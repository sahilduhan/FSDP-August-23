// find the maximum and minimum element of an array

// void type functions donot return anything
function maxMinElement(arr) {
    let size = arr.length;
    let maximumEle = Number.MIN_VALUE;
    let minimumEle = Number.MAX_VALUE;
    for(let i=0;i<size;i++){
        maximumEle = Math.max(maximumEle,arr[i]);
        minimumEle = Math.min(minimumEle,arr[i]);
    }
    return [maximumEle, minimumEle];
}


